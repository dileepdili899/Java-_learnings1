class PalindromeCheck {
    /**
     * @param args
     * @return
     */
    public static boolean main(String[] args) {
        String str = null;
        int left=0,right= str.length() -1;
        while
        (left<right){
            if(str.charAt(left)!=str.charAt(right)){
                return false;

            }
            left++;
            right--;
           }
    }
    
}
